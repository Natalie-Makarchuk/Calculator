package com.test;

public class Calculator {

        public double getSum(double a, double b) {
            return a + b;
        }

        public double getSubstraction(double a, double b) {
            return a - b;
        }

        public double getMultiply(double a, double b) {
            return a * b;
        }

        public double getDivision(double a, double b) {
            return a / b;
        }
    }


